function getACSParam($param, $device) {
  $value = $device;
  $params = explode('.', $param);
  foreach ($params as $p) {
    $value = $value[$p];
  }
  return $value;
}

function tzTimeToFormatted($strtime, $format = 'd.m.Y H:i:s')
{
  $time = strtotime($strtime);
  return date($format, $time);
}


function secondsToTime($seconds)
{
  $dtF = new \DateTime('@0');
  $dtT = new \DateTime("@$seconds");
  $diff = $dtF->diff($dtT)->format('%a gn %h saat %i dakika %s saniye');

  return str_replace([
    "0 gn", "0 saat", "0 dakika", "0 saniye"
  ], '', $diff);
}

function convertTaskName($taskName) 
{
  $taskNames = [
    'getParameterValues' => 'Parametreyi yenile',
    'setParameterValues' => 'Parametreyi gncelle',
    'refreshObject' => 'Parametreleri yenile',
    'reboot' => 'Yeniden Balat',
    'factoryReset' => 'Fabrika Ayarlarna Dn',
  ];

  return @$taskNames[$taskName];
}

function getTaskValues($task)
{
  if ($task['name'] == 'getParameterValues') {
    return $task['parameterNames'];
  }

  if ($task['name'] == 'setParameterValues') {
    return $task['parameterValues'];
  }

  if ($task['name'] == 'refreshObject') {
    return $task['objectName'];
  }

  return null;
}

